#8) Crear una lista vacía llamada "dobles" y agregar el doble de 5, 10 y 15 usando append
#directamente. Imprimir la lista resultante por pantalla.

dobles = []                          # Defino una lista vacía llamada "dobles"
dobles.append(5 * 2)                 # Agrego el doble de 5 a la lista
dobles.append(10 * 2)                # Agrego el doble de 10 a la lista
dobles.append(15 * 2)                # Agrego el doble de 15 a la lista
print(dobles)                        # Imprimo la lista resultante por pantalla